#!/usr/bin/env python3

num1 = input("Give me the first number: ")
num2 = input("Give me the second number: ")

num1 = float(num1)
num2 = float(num2)

print("Thank you!")

print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {num1 / num2}")
print(f"{num1} * {num2} = {num1 * num2}")